public class spacings
{
   public static void main(String[] args)
   {
   // System.out.print("") will print everything as one line
      System.out.print("Hi");
      System.out.print(", friend!");
      
      System.out.println("");
      System.out.println("");
      
      System.out.print("H");
      System.out.print("e");
      System.out.print("l");
      System.out.print("l");
      System.out.print("o");
      
      System.out.println("");
      System.out.println("");
    
   // When mixing these two prints, seperate with two blank lines
   // System.out.println("") will print one line at a time
   // To add spaces with println(""), add single blank line   
      System.out.println("Hi,");
      System.out.println("friend!");
     
      System.out.println("");
      
      System.out.println("H");
      System.out.println("e");
      System.out.println("l");
      System.out.println("l");
      System.out.println("o");
         
   }
}